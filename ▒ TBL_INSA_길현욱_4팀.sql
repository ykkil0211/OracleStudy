--33. TBL_INSA 테이블에서 BASICPAY + SUDANG 이 
    100만원 미만, 100만원 이상~200만원 미만, 
    200만원 이상인 직원들의 수 조회.
   
    SELECT T.수당, COUNT(*) 직원수
    FROM( 
        SELECT NAME 이름,(BASICPAY + SUDANG) "총수당",
        CASE WHEN (BASICPAY + SUDANG) < 1000000 THEN '100만원 미만'
                WHEN (BASICPAY + SUDANG) >= 1000000 AND(BASICPAY + SUDANG) < 2000000 THEN '100만원 이상 100만원 이하'
                WHEN (BASICPAY + SUDANG) >= 2000000 THEN '200만원 이상'
           ELSE '측정불가'
           END "수당"
        FROM TBL_INSA
    )T GROUP BY T.수당;
    
    /*
